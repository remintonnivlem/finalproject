
// This is a sample C++ code for a simple calculator program that takes two integers as input and returns their sum
#include <iostream>
using namespace std;
int main() {
    int num1, num2, sum;
    cout << "Enter two numbers: ";
    cin >> num1 >> num2;
    sum = num1 + num2;
    cout << "Sum of the two numbers is: " << sum;
    return 0;
}