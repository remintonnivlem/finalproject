// Example code to generate a random C++ code for your Final Project
#include <iostream>
#include <cstdlib>

int main() {
    // Generate a random number between 1 and 10
    int num = std::rand() % 10 + 1;

    // Print the result to the console
    std::cout << "The random number is: " << num << "\n";

    return 0;
}
